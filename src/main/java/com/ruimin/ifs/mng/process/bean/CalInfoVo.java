package com.ruimin.ifs.mng.process.bean;

public class CalInfoVo {
	private String id;
	private int year;
	private int sunWorkDay;
	private int sunHoliDay;
	private String def;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public int getYear() {
		return year;
	}

	public void setYear(int year) {
		this.year = year;
	}

	public int getSunWorkDay() {
		return sunWorkDay;
	}

	public void setSunWorkDay(int sunWorkDay) {
		this.sunWorkDay = sunWorkDay;
	}

	public int getSunHoliDay() {
		return sunHoliDay;
	}

	public void setSunHoliDay(int sunHoliDay) {
		this.sunHoliDay = sunHoliDay;
	}

	public String getDef() {
		return def;
	}

	public void setDef(String def) {
		this.def = def;
	}

}
